        [TestMethod]
        public void GenerateReportOnDepartureDate()
        {
            GenerateReportHandoffRecords(DateType.DepartureDate);

            InsertReportHandoff();

            //run the report
            var rpt = (UpcomingPlans)RunReport();
            var rptInfo = rpt.Globals.ReportInformation;

            //check for validity
            Assert.AreEqual(1, rptInfo.ReturnCode);
            Assert.AreEqual("", rptInfo.ErrorMessage);

            //check for total records
            var expectedTotalRecs = 8;
            var actualTotalRecs = rpt.FinalDataList.Count;
            Assert.AreEqual(expectedTotalRecs, actualTotalRecs);

            //check for first in order
            var expectedFirstFltNo = "1062";
            var actualFirstFltNo = rpt.FinalDataList[0].Fltno.Trim();
            Assert.AreEqual(expectedFirstFltNo, actualFirstFltNo, "Actual first flt no failed.");

            //check for last in order
            var expectedLastFltNo = "02225";
            var actualLastFltNo = rpt.FinalDataList.Last().Fltno.Trim();
            Assert.AreEqual(expectedLastFltNo, actualLastFltNo, "Actual last flt no failed.");

            ClearReportHandoff();
        }

